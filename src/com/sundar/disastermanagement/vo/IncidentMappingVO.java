package com.sundar.disastermanagement.vo;

public class IncidentMappingVO {
	private LocationVO location;
	private IncidentTypeVO incidentType;
	private IncidentInchargeVO incharge;
	public LocationVO getLocation() {
		return location;
	}
	public void setLocation(LocationVO location) {
		this.location = location;
	}
	public IncidentTypeVO getIncidentType() {
		return incidentType;
	}
	public void setIncidentType(IncidentTypeVO incidentType) {
		this.incidentType = incidentType;
	}
	public IncidentInchargeVO getIncharge() {
		return incharge;
	}
	public void setIncharge(IncidentInchargeVO incharge) {
		this.incharge = incharge;
	}
	

}
